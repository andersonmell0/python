# Operador Unários

print("Operador Unário '~' : ", ~10)
print("Operador Unário '+' : ", +4)
print("Operador Unário '-' : ", -4)

print('Operadores Aritimeticos')
x = 7
y = 2
print('Variavel X', x)
print('Variavel Y', y)

print("Operador Soma '+' : ", x + y)
print("Operador Subtração '-' : ", x - y)
print("Operador Multiplicação '*' : ", x * y)
print("Operador Exponenciação '**' : ", x ** y)
print("Operador Divisão '/' : ", x / y)
print("Operador Divisão Inteiro '//' : ", x // y)
print("Operador Divisão Resto '%' : ", x % y)

print('Operadores Relacioanais')
print('== ', x == y)
print('!= ', x != y)
print('< ', x < y)
print('> ', x > y)
print('<= ', x <= y)
print('>= ', x >= y)

print('Operadores Lógicos')
v = True
f = False
print('Variavel v', v)
print('Variavel f', f)
print('Operador and ', v and f)
print('Operador or ', v or f)
print('Operador and not ', v and not f)

print('Operadores Atribuição')
x=x+y
print('x=x+y', x)
x+=y
print('x+=y', x)